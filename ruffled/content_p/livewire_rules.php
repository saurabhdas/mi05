<p class="style3">Rules, Terms and Conditions:</p>
<p class="style1"> Livewire will be held in 2 stages and it is an OPEN event</p>
<p class="style1"><strong>Stage 1</strong><br>
  Eliminations where each short listed team will be given 15 minutes stage time including sound check. Bands have to perform minimum 2 songs, with <strong>atleast 1 original</strong>. Eliminations will be held between 26th -29th of December.</p>
<p class="style1"><strong> Stage 2</strong><br>
Will be the finals, where the top 2 bands selected on performance in stage 1, will get 30 minutes to perform and compete at the Open Air Theatre in front of a crowd of 10,000.</p>
<p class="style1"> Drum kits will be provided whereas all other instruments will have to be arranged for by the band.</p>
<p class="style1"> Teams overshooting the time limit will be heavily penalized.</p>
<p class="style1"> No obscenities shall be tolerated</p>
<p class="style1"> Judges&rsquo; decision is final and binding. No cribs shall be entertained</p>
<p class="style1"> There is no travel/food re-imbursement <br>
    <br>
     Accommodation will be provided at nominal rates</p>
<p class="style1"> All short listed bands shall be contacted.<br>
    <br>
     Rules shall be conveyed in detail to all the short listed bands.</p>
<p class="style1"> We reserve the right to change the rules at any time.</p>
<p class="style1"> Certificates, or proof (for competitions won etc) should be produced on demand</p>
<p class="style1">For queries contact us at &ndash; <a href="mailto:livewire@moodi.org">livewire@moodi.org</a></p>
<p class="style3">Registrations end on  15th December</p>
<p class="style1"><strong>PRIZE MONEY</strong><br>
  Lots of prize money to be won by the winning and runner up bands including best drummer, guitarist, vocalist and bassist.</p>
<p class="style1">The winning band gets over 56 hours of recording time in a professional studio to record their songs. Further, they are privileged to get a 50% discount on any extra recording sessions.<br>
