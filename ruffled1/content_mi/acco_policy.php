<h3 class="style1">Mood Indigo 2005: Dive In!</h3>
<p class="style2">Accomodation Policy</p>
<p class="style1">Mood Indigo provides accommodation to all the contingents. The accommodation would be provided in the IIT campus.</p>
<ol class="style1">
  <li>The accommodation would be provided from 25th December 0800 hrs to 30th December 1200hrs.</li>
  <li>Accommodation would be provided strictly against a valid college i-card and strictly on shared basis.</li>
  <li>The contingents would be issued mattresses, bulbs and buckets for their stay at IIT.</li>
  <li>The contingents need to deposit an amount of Rs.400 per person with us, out of which Rs. 100 would be refundable subject to following terms and conditions.</li>
  <ol>
    <li>All the items issued are returned in proper condition.</li>
    <li>No damage has been done to the room where accommodation has been provided.</li>
    <li>Rooms are vacated on or before 30 th december 1200hrs.</li>
  </ol>
</ol>
<p class="style1">&nbsp;<strong>&nbsp;Note:</strong> In case of violation of any of the above, the refundable amount may be freezed in full or part, subject to the decision of <strong>Core Group Members, Assistance and Hospitality</strong>.</p>
<p class="style1">Concession would be given to larger contingents on the following basis:-</p>
<ol class="style1"><ol>
    <li>Contingents having size between 21 and 50 would get 15% off on total non-refundable amount.</li>
    <li>Contingents having size greater than 50 would get 20% off on total non-refundable amount.</li>
  </ol>
  <p>&nbsp;</p>
</ol>
<p class="style1">&nbsp;&nbsp;<strong>Note:</strong> Refundable amount has to be deposited for each member irrespective of the contingent size.</p>
<p class="style1">&nbsp;</p>
<p class="style1"><strong>Food:</strong> We do not take any responsibility of any sort of messing or food for the contingent members during the festival. However we shall be having a full-fledged canteen catering to your needs at nominal rates. In addition a complete list of food facilities inside and outside the campus will be provided at the assistance desk at the time of registration.</p>
<p class="style1">The Mood Indigo Assistance team tries to provide conveyance to all contingents coming for Mood Indigo arriving at Dadar station, if your time and date of arrival is known to us before 17th december.</p>
<p class="style2">How To Reach IIT Bombay: </p>
<p class="style1">Our assistance volunteers would be present at Dadar station with a prominent &quot;Welcome to Mood Indigo 2005&quot; banner. Please note that conveyance would be provided on 25th December only subject to the availability of buses.</p>
<p class="style1">In case you miss out on us, you can use the following to reach IIT Bombay:-<br>
  Alight (preferably in order) at:<br>
  1. Dadar - Take a slow train on the central line to Kanjur Marg station and then take an Autorickshaw to IIT (approx fare Rs.14/-).<br>
  2. Bandra - Take BEST bus no. 422 direct to IIT Main Gate. OR go to Bandra Local Station. Take a fast local to Dadar station from Bandra and follow instructions in (a).<br>
  3. Bombay Central - Take a local train to Andheri and then take a bus (396, 392, 336) to IIT from Andheri East OR Take a local train to Dadar and follow the instructions in (a).<br>
  4. Lokmaniya Tilak Terminus(LTT) - Take a rick to IIT Main Gate. Approx fare 75-90. OR Take a rick to Kurla Local Station. Get slow local for kanjur marg.</p>
